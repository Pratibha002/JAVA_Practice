package JAVA_Practice;

public class Que15 {
    public static void main(String[] args) {
        for (int i = 0; i <= 15; i++) {
            if (i % 2 == 0) { // check even and odd
                System.out.println(+i + " IS EVEN NUMBER!!");
            } else {
                System.out.println(+i + " IS ODD NUMBER!!");
            }
        }
    }

}
